package no.nav.familie.ef.sak.behandling

object OppgaveBeskrivelse {
    val informereLokalkontorOmOvergangsstønad =
        """Bruker har søkt om overgangsstønad. Vurder oppfølging.
        
        """.trimMargin()

    val innstillingOmBrukersUtdanning =
        """
        Vi trenger en vurdering fra dere fordi bruker tar/skal ta utdanning. 
        Dere må gjøre vurderingen etter retningslinjene til folketrygdloven § 15-6 første ledd bokstav c og § 5 i Forskrift om stønad til enslig mor eller far.
        
        Dere må skrive vurderingen i et notat i Gosys med tittelen «Innstilling utdanning». 
        Når notatet er ferdigstilt, sender dere en Gosys-oppgave med tema «Enslig forsørger» til NAV Arbeid og ytelser 4489.
        
        Dere må sende oss vurderingen innen 14 dager. 
        
        Hvis dere har spørsmål om en konkret sak, kan dere avtale et møte med en saksbehandler. Dette gjør dere på Navet på siden for enslig mor eller far.
        
        Innstillingen må inneholde:
        
            · Opplysninger om tidligere utdanning og arbeidserfaring.
                - Hvis utdanningen ikke er fullført, må dere skrive det.
                
            · En vurdering av om brukeren har yrkeskompetanse og om det er nødvendig med utdanning.
                - Det er som hovedregel ikke nødvendig med ny utdanning hvis brukeren allerede har yrkeskompetanse. 
                  Vi kan bare gjøre unntak fra denne regelen hvis yrkeskompetansen er utdatert eller ikke forenlig med omsorgen for små barn. 
                  Vi legger til grunn at vanlig turnusarbeid er forenlig med omsorgen for små barn.
                - Vi kan ikke vurdere sykdom som en grunn til å ta ny utdanning.
                
            · Opplysninger om utdanningen brukeren tar/skal ta.
                - Vi trenger opplysninger om i hvilken periode brukeren skal studere og studiebelastning.
                - Vi trenger også en konkret plan for gjennomføringen og målet med utdanningen.
                
            · En vurdering av om utdanningen er hensiktsmessig for å få eller beholde arbeid.
                - Vurderingen må ta utgangspunkt i arbeidsmarkedets behov og brukerens behov/muligheter.
                - Opplysninger om utdanningen er offentlig eller privat.
                - Hvis utdanningen er privat, må dere vurdere om brukeren har særlig grunn til å velge privat utdanning. 
                  Det er fordi det offentlige utdanningstilbudet i utgangspunktet er tilstrekkelig.
                - Hvis det gjelder privat utdanning på videregående skole, må dere vurdere om brukeren har ungdomsrett eller voksenrett til offentlig videregående utdanning. 
                  Hvis brukeren ikke har en slik rett, må dette dokumenteres.
                - Vi trenger også opplysninger om brukeren har utgifter til skolepenger.
                
            · En konklusjon som svarer på om utdanningen er nødvendig og hensiktsmessig for å få eller beholde arbeid.

        """.trimIndent()
}
